    private void jtextValorProduto1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtextValorProduto1KeyReleased
        // TODO add your handling code here:
         String format = "R$ 0,00";
         String formattotal = "R$ 0,00";
         
          try {
             
             format =   NumberFormat.getCurrencyInstance().format(Double.parseDouble(jtextValorProduto1.getText()));
             labelProdutoReal.setText(format);
             double total = Double.parseDouble(jtextValorProduto1.getText())- Double.parseDouble(ValorVendaPrazo.getText());
             formattotal =   NumberFormat.getCurrencyInstance().format(Double.parseDouble(Double.toString(total)));
             if(!ValorVendaPrazo.getText().equals("0")){
              labeltrocoreal.setText(formattotal);
              }
            if(total > 0){
             labeltroco1.setText("Pendente: ");
             }else{
              labeltroco1.setText("Troco: ");
             }
        } catch (Exception e) {
            labelProdutoReal.setText("R$ "+jtextValorProduto1.getText());
            jLabeltotalapagar.setText(labelProdutoReal.getText()); labeltrocoreal.setText(ValorVendaPrazo.getText());
            
        }
    }//GEN-LAST:event_jtextValorProduto1KeyReleased

