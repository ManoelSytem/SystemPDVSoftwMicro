    public boolean ValidaVenda(){
        if (Avista.isSelected()) {
            if (jtextValorProduto1.getText().equals("") || ValorVendaPrazo.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Valor do Produto e Entrada não pode Esta Vazio");
            } else {
                novaVenda.setCodCliente(0);
                novaVenda.setValorProduto(Double.parseDouble(jtextValorProduto1.getText()));
                novaVenda.setValorEntrada(Double.parseDouble(ValorVendaPrazo.getText()));
                novaVenda.setDescricaoCompra(jTextFieldDescricao.getText());
                novaVenda.setFormaPgmt("A Vista");
                return true;
            }
        }
        if (Aprazo.isSelected()) {
            if (jtextValorProduto1.getText().equals("") || ValorVendaPrazo.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Valor do Produto e Entrada não pode Esta Vazio");
            }
            if (viewlistaCliente.idClienteSelecionado == null) {
                JOptionPane.showMessageDialog(this, "Selecione um Cliente");
            }
            if (!jtextValorProduto1.getText().equals("") && !ValorVendaPrazo.getText().equals("") && viewlistaCliente.idClienteSelecionado != null) {
                novaVenda.setCodCliente(Integer.parseInt(viewlistaCliente.idClienteSelecionado));
                novaVenda.setValorProduto(Double.parseDouble(jtextValorProduto1.getText()));
                novaVenda.setValorEntrada(Double.parseDouble(ValorVendaPrazo.getText()));
                novaVenda.setDescricaoCompra(jTextFieldDescricao.getText());
                novaVenda.setFormaPgmt("A Prazo");
                novaVenda.setValorPedente(Double.parseDouble(jtextValorProduto1.getText())-Double.parseDouble(ValorVendaPrazo.getText()));
                return true;
            }
        }
        return false;
    }
