    private void jButtExcluirCompraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtExcluirCompraActionPerformed
        // TODO add your handling code here:
        VendaBS excluirVenda = new VendaBS();
        VendaModel ex = new VendaModel();
         if(codVenda==null){JOptionPane.showMessageDialog(this, "Selecione uma Compra com Duplo Clique!");}else{
         try {
           ex.setCodVenda(Integer.parseInt(codVenda));
           excluirVenda.Excluirvenda(ex);
           CarregarGridVenda(jLabelCliente1.getText(),null);
           JOptionPane.showMessageDialog(this, ex.getMesagem());
           CarregarGridVenda(jLabelCliente1.getText(),null);
       } catch (Exception ex1) {
             JOptionPane.showMessageDialog(this, "Erro ao ExCluir Venda"+ex1);
       }
         
         }
      
    }//GEN-LAST:event_jButtExcluirCompraActionPerformed

