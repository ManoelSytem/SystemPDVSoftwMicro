    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String total = jlabelpendente.getText();
        total = total.substring(2);
        total = total.replaceAll(",", ".");
        if (total.equals("")) {
            JOptionPane.showMessageDialog(this, "Selecione uma Venda Para Pagamento!");
        }
        if (jTextFieldPagamento.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Digite o Valor de Pagamento!");

        }
        if (!jTextFieldPagamento.getText().equals("") && !total.equals("")) {
            try{
            double Pagamento = Double.parseDouble(jTextFieldPagamento.getText());
            if (Pagamento > Double.parseDouble(total)) {
                JOptionPane.showMessageDialog(this, "Valor de Pagamento, " + Pagamento + " Maior que Valor Pendente, " + total + ". \n"
                        + "Digite um Valor Menor ou igual a Valor Pendente.");
            }else{   upVenda.setValorEntrada(Pagamento+ValorEntrada);
                     upVenda.setValorPedente(Double.parseDouble(total)-Pagamento);
                     upVenda.setCodVenda(Integer.parseInt(jlabelcódígo.getText()));
                     updatevenda.vendaupdate(upVenda,Pagamento,Double.parseDouble(total));
                     JOptionPane.showMessageDialog(this,upVenda.getMesagem());
                     CarregarGridVenda(jLabelCliente1.getText(),null);
                     
                     limpar();
            }
            
            }catch(Exception e){
             JOptionPane.showMessageDialog(this,"Erro no Sistema"+ e);
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

