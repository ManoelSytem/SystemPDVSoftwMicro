  public void CarregarGridVenda(String venda, String codCli) {
     
         codCliente = codCli;      
       while (jTableclienteVenda.getModel().getRowCount() > 0) {  
           ((DefaultTableModel)jTableclienteVenda.getModel()).removeRow(0);  
      }
      try {
          DefaultTableModel modelo = (DefaultTableModel) jTableclienteVenda.getModel();
          Iterator itVendas = null;
          VendaBS vendaBS = new VendaBS();
          itVendas = vendaBS.ListaVendas(venda);
          double total = 0;
          while (itVendas.hasNext()) {

              VendaModel Vendaaux = (VendaModel) itVendas.next();
              total += Vendaaux.getValorPedente();
              Date datePadrao = (Date) formatterPadrao.parse(Vendaaux.getHoraDaVenda());
              modelo.addRow(new String[]{String.valueOf(Vendaaux.getCodVenda()), Vendaaux.getDescricaoCompra(),
                  NumberFormat.getCurrencyInstance().format(Double.parseDouble(Double.toString(Vendaaux.getValorProduto()))),
                  NumberFormat.getCurrencyInstance().format(Double.parseDouble(Double.toString(Vendaaux.getValorEntrada()))),
                  NumberFormat.getCurrencyInstance().format(Double.parseDouble(Double.toString(Vendaaux.getValorPedente()))),
                  Vendaaux.getDatadaVenda(), df.format(datePadrao), Vendaaux.getFormaPgmt()});

          }
          jLabelCliente1.setText(venda);
          jlabeltotal.setText(NumberFormat.getCurrencyInstance().format(total));
          itVendas.remove();
        } catch (Exception e) {
             
        }

    }
