        // TODO add your handling code here:
         
        try {
            
            ClienteBS Envia = new  ClienteBS();
            Cliente NovoCliente = new Cliente();
         if(Nome.getText().isEmpty()){
             JOptionPane.showMessageDialog(this,"Digite o nome do Cliente!");
         }else{
            NovoCliente.setNome(Nome.getText());
            NovoCliente.setEndereco(Endereco.getText());
            NovoCliente.setTelefone(Telefone.getText());
            NovoCliente.setCpf(Cpf.getText());
       
            Envia.adicionarCliente(NovoCliente);
          JOptionPane.showMessageDialog(this,NovoCliente.getMensagem());
          limpar();
           dispose();
           }
        } catch (Exception ex) {
            Logger.getLogger(CadCliente.class.getName()).log(Level.SEVERE, null, ex);
            limpar();
        }
